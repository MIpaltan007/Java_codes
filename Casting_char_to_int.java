import java.util.*;
public class Casting_char_to_int {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int i=(int)'A';
        System.out.println(i);
        char c=(char)65.34;
        System.out.println(c);
        int a='2'+'3';
        System.out.println(a);
        int j=2+'a';
        System.out.println(j);
    }
}
